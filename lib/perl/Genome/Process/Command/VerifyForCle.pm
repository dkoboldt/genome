package Genome::Process::Command::VerifyForCle;

use strict;
use warnings FATAL => 'all';
use Genome;

class Genome::Process::Command::VerifyForCle {
    is => 'Command::V2',
    has => [
        process => {
            is => 'Genome::Process',
            shell_args_position => 1,
            doc => 'Process to verify',
        },
    ],
    doc => 'This command will verify a process produced results that are ok for CLE',
};

sub help_detail {
    return <<EOS;
This command checks all of the results generated by the process as well as the
inputs to the process to ensure that they reside on CLE disk groups.
EOS
}

sub execute {
    my $self = shift;
    if ($self->process->is_cle_verified) {
        $self->status_message("Process (%s):  passed CLE verification",
            $self->process->id);
    } else {
        $self->status_message("Process (%s):  FAILED CLE verification",
            $self->process->id);
    }
    return 1;
}

1;
